// Fonction pour dessiner une étoile
const drawStar = (ctx, cx, cy, spikes, outerRadius, innerRadius) => {
    let rot = Math.PI / 2 * 3
    let x = cx
    let y = cy
    const step = Math.PI / spikes

    ctx.beginPath()
    ctx.moveTo(cx, cy - outerRadius)
    for (let i = 0; i < spikes; i++) {
        x = cx + Math.cos(rot) * outerRadius
        y = cy + Math.sin(rot) * outerRadius
        ctx.lineTo(x, y)
        rot += step

        x = cx + Math.cos(rot) * innerRadius
        y = cy + Math.sin(rot) * innerRadius
        ctx.lineTo(x, y)
        rot += step
    }
    ctx.lineTo(cx, cy - outerRadius)
    ctx.closePath()
}

// Fonction pour dessiner un cœur
const drawHeart = (ctx, cx, cy, size) => {
    const width = size
    const height = size

    ctx.beginPath()
    const topCurveHeight = height * 0.3
    ctx.moveTo(cx, cy + topCurveHeight)
    // Left curve
    ctx.bezierCurveTo(
        cx, cy,
        cx - width / 2, cy,
        cx - width / 2, cy + topCurveHeight
    )
    ctx.bezierCurveTo(
        cx - width / 2, cy + (height + topCurveHeight) / 2,
        cx, cy + (height + topCurveHeight) / 2,
        cx, cy + height
    )
    ctx.bezierCurveTo(
        cx, cy + (height + topCurveHeight) / 2,
        cx + width / 2, cy + (height + topCurveHeight) / 2,
        cx + width / 2, cy + topCurveHeight
    )
    // Right curve
    ctx.bezierCurveTo(
        cx + width / 2, cy,
        cx, cy,
        cx, cy + topCurveHeight
    )
    ctx.closePath()
}

