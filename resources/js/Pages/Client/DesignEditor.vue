                        ctx.rotate((element.rotation || 0) * Math.PI / 180)
                        ctx.drawImage(img, -(element.width || 0) / 2, -(element.height || 0) / 2, element.width || 100, element.height || 100)
                        ctx.restore()
                        resolve()
                    }
